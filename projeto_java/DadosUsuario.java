package projeto_java;

public class DadosUsuario {
	
	private static String Nome;
	private int NumeroPessoas = 0;
	private static int TipoLampada = 0;
	private static int SacolasPlasticas = 0;
	private static int TipoTransporte = 0;
	
	public static String getNome() {
		return Nome;
	}
	public static void setNome(String nome) {
		Nome = nome;
	}
	public int getNumeroPessoas() {
		return NumeroPessoas;
	}
	public void setNumeroPessoas(int numeroPessoas) {
		NumeroPessoas = numeroPessoas;
	}
	public static int getTipoLampada() {
		return TipoLampada;
	}
	public void setTipoLampada(int tipoLampada) {
		TipoLampada = tipoLampada;
	}
	public static int getSacolasPlasticas() {
		return SacolasPlasticas;
	}
	public void setSacolasPlasticas(int sacolasPlasticas) {
		SacolasPlasticas = sacolasPlasticas;
	}
	public static int getTipoTransporte() {
		return TipoTransporte;
	}
	public void setTipoTransporte(int tipoTransporte) {
		TipoTransporte = tipoTransporte;
	}
	
		
}
	

	
	
	
	
	
	
	